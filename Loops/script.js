//! While loops

let i = 0;

while (i <= 5) {
    console.log(`While loop iteration: ${i}`);
    i++;
}

//! Do While Loop

let k = 2
do {
    console.log(`Do-while loop iteration: ${k}`);
    k++;
} while (k < 5);

//! For Loop
for (let j = 0; j <= 5; j++) {
    console.log(`For loop iteration: ${j}`);
}